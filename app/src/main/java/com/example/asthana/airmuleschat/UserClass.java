package com.example.asthana.airmuleschat;

public class UserClass {
    private String id;
    private String name;

    public UserClass() {
    }

    public UserClass(String id, String name) {
        this.id = id;
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

}


